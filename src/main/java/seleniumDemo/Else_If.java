package seleniumDemo;

public class Else_If {

	public static void main(String[] args) {
		
		int x = 20;
		int y = 30;
		
		if (x>y) {
			System.out.println("x is grater than y");
		}else if (x>=y){
			System.out.println("x is grater than or equal to y");
			
		}else if (x<=y) {
			System.out.println("x is grater than or equal to y");
		}else {
			System.out.println("x is less than y");
		}
		

	}

}
